// Initialize i18n system
const i18n = {
    currentLang: localStorage.getItem('lumityaLang') || 'en',

    init() {
        this.setLanguage(this.currentLang);
    },

    setLanguage(lang) {
        if (!translations[lang]) {
            console.warn(`Language ${lang} not found, defaulting to English`);
            lang = 'en';
        }

        this.currentLang = lang;
        localStorage.setItem('lumityaLang', lang);
        this.updateDOM();

        // Update language button states
        document.querySelectorAll('.lb').forEach(btn => {
            btn.classList.remove('on');
            if ((lang === 'en' && btn.id.includes('en')) ||
                (lang === 'es' && btn.id.includes('es'))) {
                btn.classList.add('on');
            }
        });

        console.log('✓ Language switched to:', lang);
    },

    get(key) {
        return translations[this.currentLang]?.[key] || translations['en'][key] || key;
    },

    updateDOM() {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            el.textContent = this.get(key);
        });

        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            el.placeholder = this.get(key);
        });

        document.querySelectorAll('[data-i18n-aria]').forEach(el => {
            const key = el.getAttribute('data-i18n-aria');
            el.setAttribute('aria-label', this.get(key));
        });
    }
};

// Initialize on page load
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => i18n.init());
} else {
    i18n.init();
}
